    private class MyCanvasPrinter implements Runnable {

        JPanel g;
        Pallete p;

        public MyCanvasPrinter(JPanel g, Pallete p) {
            this.g = g;
            this.p = p;
        }

        @Override
        public synchronized void run() {
            while (g.getGraphics() == null) {
            }
            Graphics2D g2d = (Graphics2D) g.getGraphics();
            g2d.scale(0.8, 0.8);
            try {
                p.buildLabel(g2d);
            } catch (IOException ex) {
                Logger.getLogger(DetailsPalletePanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            g2d.dispose();

        }

    }

    public DetailsPalletePanel(Pallete p) {
        initComponents();
        this.p = p;
        ScheduledThreadPoolExecutor exec = new ScheduledThreadPoolExecutor(1);
        exec.schedule(new MyCanvasPrinter(jPanel1, p), 1, TimeUnit.SECONDS);
    }

